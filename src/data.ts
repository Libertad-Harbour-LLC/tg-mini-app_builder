export interface EnrollOption {
  value: string;
  label: string;
}

export const ENROLL_OPTIONS: EnrollOption[] = CATEGORIES.flatMap((cat) =>
  cat.tariffs.map((t) => ({
    value: `${cat.id}:${t.id}`,
    label: `${cat.title} — ${t.title}`,
  })),
);

export function enrollLabel(value: string): string {
  return ENROLL_OPTIONS.find((o) => o.value === value)?.label ?? value;
}

